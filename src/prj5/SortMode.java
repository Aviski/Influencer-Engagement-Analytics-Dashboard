package prj5;

/**
 * Represents how the influencers should be ordered when they're displayed in
 * the list or GUI Either alphabetically by channel name or by engagement rate
 * 
 * @author Avi Patel
 * @version 11/17/25
 */
public enum SortMode
{

    /**
     * Represents alphabetical sorting
     */
    BY_NAME,

    /**
     * Represents sorting by engagement rating
     */
    BY_ENGAGEMENT

}
