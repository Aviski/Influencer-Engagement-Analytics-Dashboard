package prj5;

/**
 * Represents the type of engagement metric to compute for an influencer
 * TRADITIONAL uses (likes + comments)/followers REACH uses (likes +
 * comments)/views
 * 
 * @author Avi Patel
 * @version 11/17/25
 */
public enum MetricType
{
    /**
     * the TRADITIONAL metric uses (likes + comments)/followers
     */
    TRADITIONAL,
    /**
     * the REACH metric uses (likes + comments)/views
     */
    REACH

}
