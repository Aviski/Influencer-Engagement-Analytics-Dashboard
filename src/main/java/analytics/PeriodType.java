package analytics;

/**
 * Represents the time period for which engagement is calculated and displayed
 * It's just in the first quarter of the year, which is jan, feb, and march
 * 
 * @author Avi Patel
 * @version 11/17/25
 */
public enum PeriodType
{

    /**
     * Represents engagement in January
     */
    JANUARY,

    /**
     * Represents engagement in Feburary
     */
    FEBRUARY,

    /**
     * Represents engagement in March
     */
    MARCH,

    /**
     * Represents engagement in Q1
     */
    Q1

}
